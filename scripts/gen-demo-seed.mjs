// Generator for the local demo seed (photos table).
//
// Produces `seeds/0001_demo_photos.sql`, a set of INSERT statements that scatter
// a handful of annotation points across every venue sub-map so the seatmap /
// masonry / Lightbox have renderable content during local development.
//
// `image_key` uses a placeholder pointing at the local placeholder SVG so the
// masonry / Lightbox can resolve *something* before real R2 uploads exist;
// `ip_hash` is a fixed demo hash (never a real IP — see prd.md Data Model).
// `created_at` is spaced out so the masonry's "newest first" order is visible.
//
// Run with:  node scripts/gen-demo-seed.mjs
// Load with: wrangler d1 execute seatmap-real --local --file=./seeds/0001_demo_photos.sql
//
// Deterministic: re-running yields identical SQL (stable ids + timestamps), so
// the committed seed file does not churn.

import { readFile, readdir, mkdir, writeFile } from "node:fs/promises";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const venuesDir = join(root, "data", "venues");
const seedsDir = join(root, "seeds");

const DEMO_IP_HASH = "demo-ip-hash-0000000000000000";
// Fixed anchor so created_at values are stable across runs: 2026-05-01T00:00:00Z.
const BASE_TS = Date.parse("2026-05-01T00:00:00Z");
const HOUR = 3_600_000;

// Crockford base32 alphabet for ULID-shaped ids (we don't need real entropy for
// demo rows, just valid-looking, unique, sortable 26-char ids).
const B32 = "0123456789ABCDEFGHJKMNPQRSTVWXYZ";
function demoUlid(seq) {
  // 10-char time component (stable) + 16-char counter from seq.
  const time = "01HZ8DEMO0".slice(0, 10);
  let n = seq + 1;
  let tail = "";
  for (let i = 0; i < 16; i += 1) {
    tail = B32[n % 32] + tail;
    n = Math.floor(n / 32);
  }
  return time + tail;
}

function sqlStr(value) {
  if (value === null || value === undefined) return "NULL";
  return `'${String(value).replace(/'/g, "''")}'`;
}

// A small pool of realistic-but-fake seat metadata per sub-map. Seat labels are
// user-authored free text (R9.5) so they stay in the user's original language.
const seatPool = [
  { seat: "1F E列 23番", event: "Spring Live 2026", date: "2026-04-12", desc: "正面、ステージまで近め。" },
  { seat: "2F C-15", event: null, date: null, desc: "視界は良好だが少し遠い。" },
  { seat: "アリーナ B4ブロック 12", event: "ARENA TOUR", date: "2026-03-20", desc: null },
  { seat: "スタンド 207 4列 88", event: null, date: "2026-02-08", desc: "柱が少し気になる位置。" },
  { seat: "1F A列 5番", event: "Fan Meeting", date: null, desc: "最前列、神席。" },
  { seat: "3F バルコニー 12-3", event: null, date: null, desc: null },
];

// Spread points across the chart so clustering has something to do.
const positions = [
  [0.34, 0.58],
  [0.62, 0.46],
  [0.5, 0.72],
  [0.28, 0.4],
  [0.7, 0.66],
  [0.46, 0.3],
];

// Varied intrinsic dimensions (px) for the compressed WebP — a mix of portrait
// / landscape / square so the masonry grid lays out at real, uneven aspect
// ratios (shape-photo-grid.md §10 "真实高于齐整"). Long edge ≤ 1920 (R5.2).
const dimensions = [
  [1080, 1440], // 3:4 portrait (phone held upright)
  [1600, 900], // 16:9 landscape
  [1200, 1200], // 1:1 square
  [1440, 1080], // 4:3 landscape
  [1080, 1920], // 9:16 tall portrait
  [1920, 1280], // 3:2 landscape
];

const files = (await readdir(venuesDir)).filter((f) => f.endsWith(".json"));
files.sort();

const lines = [
  "-- Demo seed: annotation points for local development (NOT for production).",
  "-- Generated by scripts/gen-demo-seed.mjs. Load into the LOCAL D1 only:",
  "--   wrangler d1 execute seatmap-real --local --file=./seeds/0001_demo_photos.sql",
  "--",
  "-- image_key / ip_hash are placeholders; created_at is spaced for newest-first order.",
  "-- width/height vary (portrait/landscape/square) so masonry lays out at real ratios.",
  "",
];

let seq = 0;
let rowCount = 0;

for (const file of files) {
  const venue = JSON.parse(await readFile(join(venuesDir, file), "utf8"));
  for (const subMap of venue.subMaps) {
    // 2-4 points per sub-map (varies by venue id length so it's deterministic
    // but not uniform).
    const count = 2 + (venue.id.length % 3);
    lines.push(`-- ${venue.id} / ${subMap.id}`);
    for (let i = 0; i < count; i += 1) {
      const pos = positions[(seq + i) % positions.length];
      const meta = seatPool[(seq + i) % seatPool.length];
      // Offset the dimension cycle from the position cycle so the same point
      // position can carry different aspect ratios across sub-maps.
      const dim = dimensions[(seq + i * 2) % dimensions.length];
      const id = demoUlid(seq);
      const createdAt = BASE_TS + seq * HOUR;
      const imageKey = `demo/${venue.id}/${subMap.id}/${id}.webp`;
      lines.push(
        "INSERT INTO photos " +
          "(id, venue_id, sub_map_id, x_percent, y_percent, image_key, width, height, seat_label, performance_date, event_name, description, ip_hash, created_at, deleted_at) VALUES " +
          `(${sqlStr(id)}, ${sqlStr(venue.id)}, ${sqlStr(subMap.id)}, ${pos[0]}, ${pos[1]}, ${sqlStr(imageKey)}, ${dim[0]}, ${dim[1]}, ${sqlStr(meta.seat)}, ${sqlStr(meta.date)}, ${sqlStr(meta.event)}, ${sqlStr(meta.desc)}, ${sqlStr(DEMO_IP_HASH)}, ${createdAt}, NULL);`,
      );
      seq += 1;
      rowCount += 1;
    }
    lines.push("");
  }
}

await mkdir(seedsDir, { recursive: true });
await writeFile(join(seedsDir, "0001_demo_photos.sql"), lines.join("\n"), "utf8");
console.log(`Wrote seeds/0001_demo_photos.sql with ${rowCount} demo annotation rows.`);
